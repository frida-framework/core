#!/usr/bin/env node
import fs from 'node:fs';
import path from 'node:path';
import yaml from 'yaml';
import {
  resolveSourceBootstrapManifestRel,
  resolveSourceContractLayerRel,
} from '../lib/source-contract-paths.mjs';

const ROOT_DIR = process.cwd();
const failures = [];

function readText(relativePath) {
  return fs.readFileSync(path.join(ROOT_DIR, relativePath), 'utf-8');
}

function loadYaml(relativePath) {
  return yaml.parse(readText(relativePath));
}

function fail(message) {
  failures.push(message);
}

function expectTextExcludes(relativePath, token, reason) {
  if (readText(relativePath).includes(token)) {
    fail(`${relativePath}: ${reason} (${token})`);
  }
}

function expectTextIncludes(relativePath, token, reason) {
  if (!readText(relativePath).includes(token)) {
    fail(`${relativePath}: ${reason} (${token})`);
  }
}

expectTextExcludes('templates/frida/bootloader.hbs', '.frida/templates/management/', 'bootloader must not reference undeployed management templates');
expectTextIncludes('templates/frida/bootloader.hbs', '{{auditPlaybookPath}}', 'bootloader must use repo-scoped audit playbook path');
expectTextIncludes(
  'templates/frida/bootloader.hbs',
  'standalone visualizer development repositories',
  'bootloader must preserve the standalone visualizer governance-only posture note',
);

expectTextExcludes('templates/template_app_basic/README.md', 'cd ../frida', 'template README must not depend on a local core checkout');
expectTextExcludes('templates/template_app_basic/package.json', 'file:../frida', 'template package metadata must not depend on a local core checkout');
expectTextExcludes('templates/template_app_basic/app-contract/contract.index.yaml', 'generated by FRIDA zero-start bootstrap', 'template contract must be described as derivative, not normative');

const sharedLayer = loadYaml('templates/template_app_basic/app-contract/layers/AL01-shared.yaml');
if (sharedLayer?.PATHS?.tooling?.templates) {
  fail('templates/template_app_basic/app-contract/layers/AL01-shared.yaml: PATHS.tooling.templates must not exist in target app baseline');
}
if (sharedLayer?.FRIDA_CONFIG?.paths?.templates_fridaRef) {
  fail('templates/template_app_basic/app-contract/layers/AL01-shared.yaml: FRIDA_CONFIG.paths.templates_fridaRef must not exist in target app baseline');
}
if (sharedLayer?.FRIDA_CONFIG?.paths?.templates_docsRef) {
  fail('templates/template_app_basic/app-contract/layers/AL01-shared.yaml: FRIDA_CONFIG.paths.templates_docsRef must not exist in target app baseline');
}
if (sharedLayer?.core?.pathRefs?.templatesFrida || sharedLayer?.core?.pathRefs?.templatesDocs) {
  fail('templates/template_app_basic/app-contract/layers/AL01-shared.yaml: core.pathRefs must not expose engine template roots');
}

const agentFrameworkLayer = loadYaml('templates/template_app_basic/app-contract/layers/AL02-agent-framework.yaml');
if (agentFrameworkLayer?.ZONES?.sourceCode?.purpose !== 'Primary repository source code.') {
  fail('templates/template_app_basic/app-contract/layers/AL02-agent-framework.yaml: ZONES.sourceCode.purpose must describe src/** as the primary repository code surface');
}

const taskProfiles = agentFrameworkLayer?.TASK_PROFILES || {};
const appContractEditor = taskProfiles.app_contract_editor || {};
const toolchainUpgrade = taskProfiles.frida_toolchain_upgrade || {};
const ciDebugger = taskProfiles.ci_debugger || {};
const appGovernance = taskProfiles.app_governance || {};

if (!taskProfiles.frida_toolchain_upgrade) {
  fail('templates/template_app_basic/app-contract/layers/AL02-agent-framework.yaml: TASK_PROFILES.frida_toolchain_upgrade must exist.');
}

for (const requiredEditPath of ['package.json', 'package-lock.json', '.frida/inbox/app-contract/**', 'scripts/**', 'docs/**', '.temp/**']) {
  if (!Array.isArray(toolchainUpgrade?.security?.edit_allow) || !toolchainUpgrade.security.edit_allow.includes(requiredEditPath)) {
    fail(`templates/template_app_basic/app-contract/layers/AL02-agent-framework.yaml: frida_toolchain_upgrade must include edit_allow ${requiredEditPath}`);
  }
}

for (const forbiddenPath of ['package.json', 'package-lock.json', 'scripts/**', 'docs/**']) {
  if (Array.isArray(appContractEditor?.security?.edit_allow) && appContractEditor.security.edit_allow.includes(forbiddenPath)) {
    fail(`templates/template_app_basic/app-contract/layers/AL02-agent-framework.yaml: app_contract_editor must not gain ${forbiddenPath}`);
  }
}

for (const requiredEditorPath of ['.frida/inbox/app-contract/**', '.temp/**']) {
  if (!Array.isArray(appContractEditor?.security?.edit_allow) || !appContractEditor.security.edit_allow.includes(requiredEditorPath)) {
    fail(`templates/template_app_basic/app-contract/layers/AL02-agent-framework.yaml: app_contract_editor must keep ${requiredEditorPath}`);
  }
}

for (const forbiddenCiPath of ['.frida/inbox/app-contract/**', 'package-lock.json']) {
  if (Array.isArray(ciDebugger?.security?.edit_allow) && ciDebugger.security.edit_allow.includes(forbiddenCiPath)) {
    fail(`templates/template_app_basic/app-contract/layers/AL02-agent-framework.yaml: ci_debugger must not gain ${forbiddenCiPath}`);
  }
}

for (const forbiddenGovernancePath of ['.frida/inbox/app-contract/**', 'package.json', 'package-lock.json']) {
  if (Array.isArray(appGovernance?.security?.edit_allow) && appGovernance.security.edit_allow.includes(forbiddenGovernancePath)) {
    fail(`templates/template_app_basic/app-contract/layers/AL02-agent-framework.yaml: app_governance must not gain ${forbiddenGovernancePath}`);
  }
}

const sourceCodeConstraints = Array.isArray(agentFrameworkLayer?.ZONES?.sourceCode?.constraints)
  ? agentFrameworkLayer.ZONES.sourceCode.constraints
  : [];
for (const requiredConstraint of [
  'In standalone visualizer development repositories, src/** is a code-only surface. Frida governs workflow here but is not the recipient of implementation activity.',
  'Do not reshape src/** around .frida/**, app-contract authoring, or FRIDA ecosystem embedding unless an explicit later integration task authorizes it.',
]) {
  if (!sourceCodeConstraints.includes(requiredConstraint)) {
    fail(`templates/template_app_basic/app-contract/layers/AL02-agent-framework.yaml: missing sourceCode constraint (${requiredConstraint})`);
  }
}

const managementLayerPath = resolveSourceContractLayerRel('FL11-management.yaml', ROOT_DIR);
const managementLayer = readText(managementLayerPath);
for (const token of ['source_playbook_ref:', 'deployed_playbook_ref:']) {
  if (!managementLayer.includes(token)) {
    fail(`${managementLayerPath}: missing repo-scoped playbook metadata (${token})`);
  }
}
for (const token of ['AGENT-contract-update.md', 'AGENT-contract-repair.md', 'AGENT-task-formulation.md']) {
  if (managementLayer.includes(token)) {
    fail(`${managementLayerPath}: legacy shared playbook name must be removed (${token})`);
  }
}
for (const token of [
  'instruction_contract:',
  'AGENT-app-contract-update.md',
  'AGENT-app-contract-repair.md',
  'AGENT-frida-toolchain-upgrade.md',
  'AGENT-task-intake.md',
  'AGENT-task-setter.md',
  'AGENT-task-validation.md',
  'AGENT-task-tracking.md',
]) {
  if (!managementLayer.includes(token)) {
    fail(`${managementLayerPath}: missing interface-instruction contract metadata (${token})`);
  }
}

const bootstrapManifestPath = resolveSourceBootstrapManifestRel(ROOT_DIR);
const bootstrapManifest = loadYaml(bootstrapManifestPath);
const entries = Array.isArray(bootstrapManifest?.entries) ? bootstrapManifest.entries : [];
const findEntry = (id) => entries.find((entry) => entry?.id === id);
const appUpdateEntry = findEntry('playbook_app_contract_update');
const appRepairEntry = findEntry('playbook_app_contract_repair');
const toolchainUpgradeEntry = findEntry('playbook_frida_toolchain_upgrade');
const taskIntakeEntry = findEntry('playbook_task_intake');
const taskSetterEntry = findEntry('playbook_task_setter');
const taskValidationEntry = findEntry('playbook_task_validation');
const taskTrackingEntry = findEntry('playbook_task_tracking');

for (const [id, entry, target] of [
  ['playbook_app_contract_update', appUpdateEntry, '.frida/contract/playbooks/AGENT-app-contract-update.md'],
  ['playbook_app_contract_repair', appRepairEntry, '.frida/contract/playbooks/AGENT-app-contract-repair.md'],
  ['playbook_frida_toolchain_upgrade', toolchainUpgradeEntry, '.frida/contract/playbooks/AGENT-frida-toolchain-upgrade.md'],
  ['playbook_task_intake', taskIntakeEntry, '.frida/contract/playbooks/AGENT-task-intake.md'],
  ['playbook_task_setter', taskSetterEntry, '.frida/contract/playbooks/AGENT-task-setter.md'],
  ['playbook_task_validation', taskValidationEntry, '.frida/contract/playbooks/AGENT-task-validation.md'],
  ['playbook_task_tracking', taskTrackingEntry, '.frida/contract/playbooks/AGENT-task-tracking.md'],
]) {
  if (!entry) {
    fail(`${bootstrapManifestPath}: missing manifest entry ${id}`);
    continue;
  }
  if (entry.target !== target) {
    fail(`${bootstrapManifestPath}: ${id} must target ${target}`);
  }
  if (entry.ownership_class !== 'user_owned') {
    fail(`${bootstrapManifestPath}: ${id} must be user_owned`);
  }
  if (entry.apply_mode !== 'seed_if_absent') {
    fail(`${bootstrapManifestPath}: ${id} must use seed_if_absent`);
  }
}

for (const entry of entries) {
  if (typeof entry?.target !== 'string') continue;
  if (entry.target.includes('AGENT-frida-internal-contract-') && !entry.cleanup_only) {
    fail(`${bootstrapManifestPath}: internal Frida playbooks must not be deployed to target repos`);
  }
  if (
    entry.target.includes('AGENT-contract-update.md') ||
    entry.target.includes('AGENT-contract-repair.md') ||
    entry.target.includes('AGENT-task-formulation.md')
  ) {
    if (!entry.cleanup_only) {
      fail(`${bootstrapManifestPath}: legacy playbook targets must be cleanup-only`);
    }
  }
}

for (const internalPlaybook of [
  'templates/management/AGENT-frida-internal-contract-update.md',
  'templates/management/AGENT-frida-internal-contract-repair.md',
]) {
  if (fs.existsSync(path.join(ROOT_DIR, internalPlaybook))) {
    fail(`${internalPlaybook}: internal playbook must live under core-templates/management`);
  }
}

if (failures.length > 0) {
  console.error('❌ Surface isolation check failed:');
  for (const failure of failures) {
    console.error(`   - ${failure}`);
  }
  process.exit(1);
}

console.log('✅ Surface isolation check passed');
