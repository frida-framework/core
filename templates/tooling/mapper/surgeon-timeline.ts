const contractBadgeContent = `import React from 'react';

interface Props {
  complexity: number; // 1-10
  size?: 'sm' | 'md';
}

function getComplexityInfo(complexity: number): { colorClass: string; label: string } {
  if (complexity <= 3) {
    return { colorClass: 'bg-[#9CA3AF]/20 text-[#9CA3AF] border-[#9CA3AF]/30', label: 'Easy' };
  }
  if (complexity <= 5) {
    return { colorClass: 'bg-[#60A5FA]/20 text-[#60A5FA] border-[#60A5FA]/30', label: 'Moderate' };
  }
  if (complexity <= 7) {
    return { colorClass: 'bg-[#3B82F6]/20 text-[#3B82F6] border-[#3B82F6]/30', label: 'Hard' };
  }
  return { colorClass: 'bg-[#8B5CF6]/20 text-[#8B5CF6] border-[#8B5CF6]/30', label: 'Extreme' };
}

const ComplexityBadge: React.FC<Props> = ({ complexity, size = 'md' }) => {
  const { colorClass, label } = getComplexityInfo(complexity);
  const sizeClass = size === 'sm' ? 'text-xs px-2 py-0.5' : 'text-sm px-3 py-1';

  return (
    <span className={\`inline-flex items-center justify-center rounded-full border \${colorClass} \${sizeClass} font-medium\`}>
      {label} ({complexity})
    </span>
  );
};

export default ComplexityBadge;
`;
writeFile(`${OUTPUT_DIR}/components/ComplexityBadge.tsx`, contractBadgeContent);

